def _load_keypair(path: str) -> Keypair:
    """Load a Solana CLI–format keypair file (JSON array of 64 ints)."""
    import json as _json

    raw = _json.loads(Path(path).expanduser().read_text())
    if not isinstance(raw, list) or len(raw) not in (64, 65):
        raise ValueError(f"unexpected keypair format at {path}")
    return Keypair.from_bytes(bytes(raw[:64]))


def _derive_ws_url(rpc_url: str) -> str:
    if rpc_url.startswith("https://"):
        return "wss://" + rpc_url[len("https://"):]
    if rpc_url.startswith("http://"):
        return "ws://" + rpc_url[len("http://"):]
    return rpc_url
