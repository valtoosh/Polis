"""
solana_client.py
================

Async client for the polis on-chain programs, driven by anchorpy + the IDLs
generated by `anchor build`.

All instruction encoding (discriminator + borsh args), account decoding, and
event parsing flow through `anchorpy.Program` and `anchorpy.EventParser`. This
module is responsible for:

* Loading the IDLs and instantiating one ``Program`` per polis program.
* Deriving PDAs (seeds documented inline; verified against the IDL `pda.seeds`).
* Building + submitting instructions on behalf of the agent operator.
* Reading account state (`CreditLine`, `VouchPool`, `Wallet`, `RepStats`).
* Subscribing to the x402 program logs and decoding `PaymentReceived` events
  into a local payment cache used by `/summarize`.

The public surface (`register_agent`, `pay_for_service`, `transfer_out_wallet`,
`deposit_to_pool`, ..., `vouch`, `unvouch_request`, `slash`) is the same as
the pre-IDL revision so the rest of the agent (treasury_manager.py,
credit_manager.py, server.py) is unchanged.
"""
